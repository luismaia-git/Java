
/**
 * Conceitos de Variável
 * @author Luís Antonio */ 

public class Variavel {
    /* Declaração das variáveis*/
    
    public static void main (String[] args) {
        String nome = "Luís Antonio";
        int idade = 20;
        boolean namorando = true;
        /* Imprime em tela o resultado da variavel*/
        if(idade >= 20 && namorando) {
            System.out.println(nome);
        }
        
    } //fim do método
}//fim da classe